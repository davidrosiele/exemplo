public class exercicio13 {

    public static void main(String[] args) {
        int i = 0;
        while (i<=100){
            if (i%2 == 0){
                System.out.println("O numero: "+i +"E par" );
            }else{
                System.out.println("O numero: "+i +"E impar");
            }
            i++;
        }
    }
}
